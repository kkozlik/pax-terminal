color_t *init_draw_buffer(){
    color_t *buffer = (color_t *)malloc(SCREEN_WIDTH * SCREEN_HEIGHT * BPP);
    if (!buffer) {
        perror("Error allocating memory for buffer");
        fflush(stderr);
        return NULL;
    }

    return buffer;
}

void free_draw_buffer(color_t *buffer){
    free(buffer);
}

/**
 * Flush buffer to screen
 * @param color_t *buffer
 * @param color_t *fbp          Framebuffer
 */
void draw_buffer(color_t *buffer, color_t *fbp){
    memcpy(fbp, buffer, SCREEN_WIDTH * SCREEN_HEIGHT * BPP);
}

void draw_pixel(color_t *buffer, int x, int y, color_t color) {
    if (y >= 0 && y < SCREEN_WIDTH && x >= 0 && x < SCREEN_HEIGHT) {
        buffer[(SCREEN_WIDTH * SCREEN_HEIGHT) -
               ((SCREEN_HEIGHT - x) * SCREEN_WIDTH + y)] = color;
    }
}

void draw_line(color_t *buffer,int x0,int y0,int x1,int y1,color_t color) {
    int dx = abs(x1 - x0), sx = (x0 < x1) ? 1 : -1;
    int dy = -abs(y1 - y0), sy = (y0 < y1) ? 1 : -1;
    int err = dx + dy, e2;

    for (;;) {
        draw_pixel(buffer,x0,y0,color);
        if (x0 == x1 && y0 == y1) break;
        e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; }
        if (e2 <= dx) { err += dx; y0 += sy; }
    }
}

void draw_circle(color_t *buffer,int xc,int yc,int r,color_t color) {
    int x = r, y = 0;
    int err = 0;
    while (x >= y) {
        draw_pixel(buffer, xc + x, yc + y, color);
        draw_pixel(buffer, xc + y, yc + x, color);
        draw_pixel(buffer, xc - y, yc + x, color);
        draw_pixel(buffer, xc - x, yc + y, color);
        draw_pixel(buffer, xc - x, yc - y, color);
        draw_pixel(buffer, xc - y, yc - x, color);
        draw_pixel(buffer, xc + y, yc - x, color);
        draw_pixel(buffer, xc + x, yc - y, color);
        y++;
        if (err <= 0) {
            err += 2 * y + 1;
        } else {
            x--;
            err -= 2 * x + 1;
        }
    }
}

void clear_buffer(color_t *buffer, color_t color) {
    int pixels = SCREEN_WIDTH * SCREEN_HEIGHT;
    for (int i = 0; i < pixels; i++) buffer[i] = color;
}

void draw_char(color_t *buffer,int x,int y,char c,color_t color) {
    unsigned char uc = (unsigned char)c;
    if (uc == 0 || uc >= 128) return;
    const uint8_t *glyph = font8x8_basic[uc - 1];

    for (int row = 0; row < 8; row++) {
        for (int col = 0; col < 8; col++) {
            if ((glyph[col] >> row) & 1) {
                draw_pixel(buffer, x + row, y + col, color);
            }
        }
    }
}

void draw_text(color_t *buffer,int x,int y,const char *text,color_t color) {
    while (*text) {
        draw_char(buffer,x,y,*text,color);
        x += 8;
        text++;
    }
}

// 7-seg digits
void draw_digit(color_t *buffer,int x,int y,int digit) {
    if (digit < 0 || digit > 10) return;
    for (int col = 4; col >= 0; col--) {
        uint16_t segment = segments[digit][col];
        for (int row = 0; row < 15; row++) {
            if (segment & (1 << row)) {
                for (int j = 0; j < 3; j++) {
                    for (int i = 0; i < 3; i++) {
                        draw_pixel(
                            buffer,
                            (row + y) * 3 - j + 3,
                            (x   - col) * 3 - i,
                            0xFFFF
                        );
                    }
                }
            }
        }
    }
}

uint16_t rgb24_to_rgb565(uint8_t r, uint8_t g, uint8_t b) {
    return ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3);
}

/**
 * Draw BMP image at given position in buffer
 */
void draw_bmp_image(color_t *buffer, int offset_x, int offset_y, BITMAPINFOHEADER bih, uint8_t *bmp_data) {
    int width = bih.biWidth;
    int height = bih.biHeight;
    int pad = (4 - (width * 3) % 4) % 4;

    for (int x = 0; x < height && (x + offset_x) < SCREEN_HEIGHT; ++x) {
        int src_x = height - 1 - x;

        for (int y = 0; y < width; ++y) {
            int dst_y = SCREEN_WIDTH - 1 - (y + offset_y);
            if (dst_y < 0 || dst_y >= SCREEN_WIDTH) continue;

            int src_y = width - 1 - y;
            int bmp_index = (src_x * (width * 3 + pad)) + (src_y * 3);
            uint8_t b = bmp_data[bmp_index];
            uint8_t g = bmp_data[bmp_index + 1];
            uint8_t r = bmp_data[bmp_index + 2];

            uint16_t pixel565 = rgb24_to_rgb565(r, g, b);

            int buffer_index = ((x + offset_x) * SCREEN_WIDTH) + dst_y;
            buffer[buffer_index] = pixel565;
        }
    }
}
